#include "../libft.h"

char	*ft_strdup(const char *str)
{
	int		len;
	char	*new_str;

	len = ft_strlen(str) + 1;
	new_str = malloc(len);
	if (new_str == NULL)
	{
		return (NULL);
	}
	ft_memcpy(new_str, str, len);
	return (new_str);
}
