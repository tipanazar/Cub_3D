#include "../libft.h"

int	ft_type_identifier(char ch)
{
	if (ch == 'c' || ch == 's' || ch == 'p' || ch == 'd' || ch == 'i'
		|| ch == 'u' || ch == 'x' || ch == 'X' || ch == '%')
		return (1);
	return (0);
}
