#include "../libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t num)
{
	char	*p_dest;
	char	*p_src;

	if (!dest && !src)
		return (NULL);
	p_dest = (char *)dest;
	p_src = (char *)src;
	while (num--)
	{
		*p_dest++ = *p_src++;
	}
	return (dest);
}
